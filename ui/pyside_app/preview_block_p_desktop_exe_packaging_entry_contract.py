def _copy_plain(value: Any) -> Any:
    if type(value) is dict:
        return {key: _copy_plain(item) for key, item in value.items()}
    if type(value) is list:
        return [_copy_plain(item) for item in value]
    return value


def _all_plain_json(value: Any, max_depth: int | None = None) -> bool:
    stack: list[tuple[Any, int, bool]] = [(value, 0, False)]
    active: set[int] = set()
    while stack:
        current, depth, leave = stack.pop()
        if type(current) in (dict, list):
            ident = id(current)
            if leave:
                active.remove(ident)
                continue
            if ident in active:
                return False
            if max_depth is not None and depth > max_depth:
                return False
            active.add(ident)
            stack.append((current, depth, True))
            if type(current) is dict:
                for key, item in current.items():
                    if type(key) is not str:
                        return False
                    stack.append((item, depth + 1, False))
            else:
                for item in current:
                    stack.append((item, depth + 1, False))
        elif type(current) in (str, int, bool) or current is None:
            if type(current) is float:
                return False
        else:
            return False
    return True


def _exact_plain_matches(value: Any, expected: Any) -> bool:
    if not _all_plain_json(value, MAX_DIAGNOSTIC_CONTAINER_DEPTH):
        return False
    if type(value) is not type(expected):
        return False
    if type(expected) is dict:
        if list(value.keys()) != list(expected.keys()):
            return False
        return all(_exact_plain_matches(value[key], expected[key]) for key in expected)
    if type(expected) is list:
        if len(value) != len(expected):
            return False
        return all(_exact_plain_matches(item, expected[index]) for index, item in enumerate(value))
    return value == expected


def _plain_dict_section(source: dict[str, Any], field: str) -> dict[str, Any]:
    value = source.get(field)
