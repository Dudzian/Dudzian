def _owned_fields_are_unshadowed(section: Any, expected: dict[str, Any], owned: list[str]) -> bool:
    if type(section) is not dict:
        return False
    seen: set[str] = set()
    for key, value in section.items():
        if type(key) is not str:
            continue
        if key in owned:
            if key not in expected:
                return False
            if key in seen:
                return False
            if not _exact_plain_matches(value, expected[key]):
                return False
            seen.add(key)
    return True


